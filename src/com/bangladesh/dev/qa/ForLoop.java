package com.bangladesh.dev.qa;

public class ForLoop {
    public static void main(String[] args) {

        int i = 1;
        for (i = 1; i <= 10; i++) {
            System.out.println(i);

        }
        System.out.println("==================");
        int x = 10;
        for (x = 10; x >= 1; x--) {
            System.out.println(x);
        }


    }
}
