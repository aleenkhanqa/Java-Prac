public class DoWhileLoop {

    public static void main(String[] args) {

       int i = 1;
       do {
           System.out.println(i);
           i=i+1;

       }while (i<=10);

        int b = 2;
        do {
            System.out.println(b);
            b = b + 1;
        } while (b <= 30);
    }
}
