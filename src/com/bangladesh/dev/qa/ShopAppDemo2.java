public class ShopAppDemo2 {

    public static void main(String[] args) {


        int measurement = 1;


        switch (measurement) {
            case 1:
                System.out.println("Size will be s");
                break;
            case 2:
                System.out.println("Size will be m");
                break;
            case 3:
                System.out.println("Size will be l");
                break;
            default:
                System.out.println("Size will be x");


        }


    }


}
